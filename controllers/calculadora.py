def soma(n1=0, n2=0):
    resultado = float(n1) + float(n2)
    return resultado


def subtracao(n1=0, n2=0):
    resultado = float(n1) - float(n2)
    return resultado


def multiplicacao(n1=0, n2=0):
    resultado = float(n1) * float(n2)
    return resultado


def divisao(n1=0, n2=0):
    resultado = float(n1) / float(n2)
    return resultado
